template <>
struct TTKTraits<ttkHelloWorld> {
    static constexpr std::string_view className = "ttkHelloWorld";
    static constexpr std::string_view identifier = "ttkHelloWorld";
    static constexpr std::string_view displayName = "TTK HelloWorld";
    static constexpr std::string_view category = "topology";
    static constexpr std::string_view tags = "TTK";
    inline static std::array<InputData, 1> inports = {InputData{
        "Input", "vtkDataSet", 1,
        R"(A vtkDataSet that has at least one point data scalar array that can be used to compute averages.)"}};
    inline static std::array<OutputData, 0> outports = {};
    inline static std::array<Group, 3> groups = {
        Group{"Input options", {"InputArray"}}, Group{"Output options", {"OutputArrayName"}},
        Group{"Testing",
              {"Debug_UseAllCores", "Debug_ThreadNumber", "Debug_DebugLevel",
               "CompactTriangulationCacheSize", "Debug_Execute"}}};
    std::tuple<Wrapper0, Wrapper1, Wrapper2, Wrapper3, Wrapper4, Wrapper5, Wrapper6> properties;
    static constexpr std::string_view doc =
        R"(This filter is a well documented ttk example filter that computes for each vertex of a vtkDataSet the average scalar value of itself and its neighbors.)";
};

void registerttkHelloWorld(InviwoModule* module) {
    module->registerProcessor<TTKGenericProcessor<ttkHelloWorld>>();
}

